void	*memccpy(void *dest, const void *src, int c, size_t n)
{
	int		i;

	i = 0;
	while (i <= n)
	{
		if (src[i] == c)
			return (&dest[i]);
		dest[i] = src[i];
	}
	return (NULL);
}

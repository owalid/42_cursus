void		fdf_display_line(t_ord *ord)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	ord->dy = ord->y1 - ord->y2;
	ord->dx = ord->x1 - ord->x2;
	while (x >= ord->x1 || x <= ord->x2)
	{
		fdf_draw_line(x, y);
		x++;
	}
}

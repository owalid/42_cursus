char		*ft_strupcase(char *str)
{
	int		i;
	char	*result;

	i = 0;
	result = str;
	while (result[i] != '\0')
	{
		if (result[i] >= 97 && result[i] <= 122)
			result[i] -= 32;
		i++;
	}
	return (result);
}
